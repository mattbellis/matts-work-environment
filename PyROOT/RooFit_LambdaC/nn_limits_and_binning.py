def ntp_to_sp(baryon, ntp):
    
    sp = -1

    # Allow the user to not add the 'ntp' part

    if baryon=='LambdaC':
        if ntp=='ntp1':
            sp = '9446'
        elif ntp=='ntp2':
            sp = '9445'
    elif baryon=='Lambda0':
        if ntp=='ntp3':
            sp = '9452'
        elif ntp=='ntp4':
            sp = '9453'
        elif ntp=='ntp1':
            sp = '9454'
        elif ntp=='ntp2':
            sp = '9455'

    return sp

